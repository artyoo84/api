package oop05.polymorphism;

public class RpsVO {
	int rock, paper, scissors; // 바위 보 가위.

	public int getRock() {
		return rock;
	}

	public void setRock(int rock) {
		this.rock = rock;
	}

	public int getPaper() {
		return paper;
	}

	public void setPaper(int paper) {
		this.paper = paper;
	}

	public int getScissors() {
		return scissors;
	}

	public void setScissors(int scissors) {
		this.scissors = scissors;
	}

}
