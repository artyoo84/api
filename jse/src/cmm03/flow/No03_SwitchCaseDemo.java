package cmm03.flow;

import java.lang.invoke.SwitchPoint;
import java.util.Scanner;

public class No03_SwitchCaseDemo {
	public static void main(String[] args) {
		//System.out.println("평균 점수를 입력하세요");
		Scanner scanner = new Scanner(System.in);
		int avg = 0;
		do
		{
			System.out.println("평균 점수를 입력하세요");
			avg = scanner.nextInt();
		} while(avg > 100);
		
		String result = "";
		
			/*if (avg > 100){
			System.out.println("다시 입력하세요");
			avg = scanner.nextInt();
		}*/
		
		if (avg >= 70) {
			result = "합격";
		}
		if (avg >= 70) {
			result = "합격";
		} else {
			result = "메시지";
		}
		
		// 평균 점수에 따라서 비교 학점구하기

		/*
		 * if (avg >= 90) { grade = 'A'; } else if (avg >= 80) { grade = 'B'; }
		 * else if (avg >= 70) { grade = 'C'; } else if (avg >= 60) { grade =
		 * 'D'; } else { grade = 'F'; }
		 */
		char grade = 'F';
		switch (avg / 10) {
		case 9: grade = 'A'; break;
		case 8: grade = 'B'; break;
		case 7: grade = 'C'; break;
		case 6: grade = 'D'; break;
		default:grade = 'F'; break;
		}

		// 위 if else # Switch # 바꾸시오
		System.out.println("귀하는 " + result + " 이며, 학점은 " + grade + " 입니다.");
	}
}
