package cmm02.op;

public class No04_CanditionDemo {
	public static void main(String[] args) {

		int val01 = 1;
		int val02 = 2;

		if ((val01 == 1) && (val02 == 2)) {
			System.out.println("1번결과 2번결과 true");
		}
		if ((val01 != 1) || (val02 != 2)) {
			System.out.println("1번 결과 2번결과 false");
		}
		
	}
}
